test_that("FDate rela", {
	expect_true(as.FDate("2009-02-28") != as.FDate("2009-02-29"))
	expect_true(as.FDate("2009-02-28") <= as.FDate("2009-02-29"))
	expect_true(as.FDate("2009-02-28") < as.FDate("2009-02-29"))
	expect_true(as.FDate("2009-A") != as.FDate("2009-01"))
	expect_true(as.FDate("2009-A") <= as.FDate("2009-01"))
	expect_true(as.FDate("2009-A") < as.FDate("2009-01"))
	expect_true(as.FDate("2009-Q1") != as.FDate("2009-S1"))
	expect_true(as.FDate("2009-Q1") >= as.FDate("2009-S1"))
	expect_true(as.FDate("2009-Q1") > as.FDate("2009-S1"))
	expect_false(as.FDate("2009-01-30") == as.FDate("2009-01-31"))
	expect_false(as.FDate("2009-02-29") == as.FDate("2009-03"))
	expect_false(identical(as.FDate("2009-02-28"), as.FDate("2009-02-29")))
	expect_false(identical(as.FDate("2009-A"), as.FDate("2009-01")))
})
